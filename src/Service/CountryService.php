    public static function getTransVoucherCountryCodes(): array
    {
        return $GLOBALS['TV_COUNTRY_CODES']
            ?? $GLOBALS['TV_COUNTRY_CODES'] = array_map(function (array $country) {
            return $country['code'];
        }, self::getTransVoucherCountries());
    }

    public static function isCountryTransVoucherSupported(string $countryCode): bool
    {
        return in_array($countryCode, self::getTransVoucherCountryCodes());
    }
